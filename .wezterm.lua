local wezterm = require 'wezterm'
local config = {}

-- system settings
config.enable_wayland = true

-- font settings
config.font = wezterm.font 'Maple Mono NF'
config.font_size = 14

-- window settings
config.use_fancy_tab_bar = false
config.color_scheme = 'Everforest Dark Medium (Gogh)'
